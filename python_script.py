# Создание списка
lst = [1, 2, 3, 4, 5]

# Добавление элемента в конец
lst.append(6)

# Вставка элемента по индексу
lst.insert(2, 10)

# Удаление элемента по значению
lst.remove(3)

# Удаление элемента по индексу
del lst[0]

# Обратный порядок
lst.reverse()

# Длина списка
length = len(lst)

print("Обновленный список:", lst)
print("Длина списка:", length)

